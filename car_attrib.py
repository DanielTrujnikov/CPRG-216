class Car:
    def __init__(self, make, model, year, colour, mileage, body, status):
        self.make = make
        self.model = model
        self.year = year
        self.colour = colour
        self.mileage = mileage
        self.body = body
        self.status = status

def accelerate(self):
    self.speed += 5

def decelerate(self):
    self.speed += 10

def brake(self):
    self.speed = 0